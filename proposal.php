                    <h4>
                        <strong>Summary
                            <br>
The British Columbia Institute of Technology is a rapidly expanding institute with a large multitude of students enrolled on its main campus, and professors specializing in a variety of subjects. As a result, communication is highly crucial to keep both the students and instructors connected and informed on any unexpected instances. Using the Spark wave Chat Application, a cloud-based chat platform, would benefit both the staff and students without placing a huge dent in the institute’s budget. Some of the benefits included are:
•	Money from the institute’s budget would not be required, as all the fees would be paid by the students monthly subscription
•	Allow instructors and students to remain in constant communication

Integrating the chat platform into the school system would be broken up into 4 stages. This is to allow students and faculty plenty of time to learn the platform and transition easily into it. During the three weeks, the semester would still run as normal, but with the added benefit of giving students an easier way to get in touch with their instructors.

Stage I should begin at the end of the month (March, 2016), to ensure that all students and faculty are present just before finals exam week begins and students are more concerned with studying.

<br>
<br>

Introduction/Background
<br>                        
As an institute of higher learning with over 100 instructors teaching a multitude of courses, communication has always been important between the students and professors, if anything unexpected happens with the professor. With this in mind, if a professor ends up extremely ill or incapacitated enough that they require another staff member to send a mass e-mail out to the students, informing them that a class is cancelled, there is an off-chance the staff member forgets to send the e-mail to the students. Should this happen, and the students end up attending the class, resulting in wasted effort and time for all, a more efficient and reliable way of informing students of sudden events regarding a professor and how it affects them is needed. E-mailing students is the most common used method, but is not completely reliable, as it relies on not only the professor being physically able-bodied to access their computer and send an e-mail out, but also relying on another staff member to actually remember to send the e-mail out, if the professor delegates the task off.


The institute needs a way for professors to easily inform their students of any unforeseen circumstances, in a cost-effective and simple manner. Implementing the Spark wave Chat Application would provide a large amount of benefits that would make it easier for professors to communicate with their students in each course they teach, and make it quicker for them to inform the students of any unplanned cancellations in a lab or lecture. Through introducing and implementing the chat application, the institute can:

•	Swiftly notify students of cancellations of labs and lectures
•	Allows instructors to keep in contact with students
•	Instructors can create chat rooms for each course they teach
•	Students can easily ask instructors any questions they have on a particular topic

Although the chat platform is relatively new, it is constantly being updated to improve chat performance and reliability, which benefits more than hinders the overall quality of the chat platform and its usage.
